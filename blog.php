                            <div class = "text-center" style='text-align:center;'>
                                <img src="img/b2.jpeg" alt="Dr. Dog" height="400"><br>
                                </div>
                            <br><br>It is said that one donation can save upto three lives. It is especially neccessary since blood cannot a this point be created synthetically and the shelf life is also not long enough to store large quantities.<br>
                            
                            <br>People donate blood to help others, to do what's in their right and have a huge impact. It is a powerful act indeed, but besides these great and most important purposes, there could be a whole lot more benefits to donating blood!<br>
                            
                            <br><h4>1. Healthy Heart</h4><br>
                            
                            It has been observed that regular donation of blood is linked to healthier vascular systems. This is because donating blood can hep lowering blood pressure and also lower risk of heart attacks. Donating blood can lower the viscosity of Blood if the hemoglobin is too high, essentially preventing blood clots. <br>
                            
                            <br><h4>2. Free health screening</h4><br>
                            
                            Before you donate, your vitals will bw checked. This exam might turn up a condition that needs attention such as high blood pressure or a heart arrhythmia like atrial fibrillation. In addition, you'll be screened for infectious diseases you may be unaware of.<br>
                            
                            <br><h4>3. A happier life</h4><br>
                            
                            People usually donate because it feels good to help others, and altruism and volunteering have been linked to positive health outcomes, including a lower risk for depression and greater longevity.<br>
                            
                            <br><br><h3><b>Requirements</b></h3><br>
                            
                            <br>Any healthy individual, regardless of gender, can donate blood. Even still, there are certain restrictions to keep in mind before you finalize your decision.<br>
                            
                            <br><h4>1. Hemoglobin</h4><br>
                            <div class = "text-center" style='text-align:center;'>
                                <img src="img/b3.avif" alt="Red blood cells" height="300"><br>
                                </div>
                                <br><br>If you're planning to donate, it is a must that you have at least 12.5 grams per deciliter of Hemoglobin. Any less than that and it is not considered fit to be received.<br>
                            
                            <br><h4>2. Age</h4><br>
                            <div class = "text-center" style='text-align:center;'>
                                <img src="img/b4.avif" alt="Elderly people" height="400"><br>
                                </div><br>
                                <br><br>Donors in India are required to be legal Adults to be eligible for donation. There is also an upper age limit to it, though!
                            People older than 65 also cannot donate.<br><br>
                            
                            <br><h4>3. Tattoos</h4><br>
                            <div class = "text-center" style='text-align:center;'>
                                <img src="img/b5.avif" alt="tattoo" height="300"><br>
                                </div><br>
                                <br><br>It is important to calculate the time passed since your most recent tattoo if you are thinking of donating blood.<br>
                            It is a generally good rule of thumb to assume that you won't be able to donate after at least three months of getting a tattoo.<br>
                            
                            <br>This does not have to do with the misconception of ink in the blood ruining the sample, but more with the needle used during the process. An unclean needle may carry harmful viruses and unknowingly transmit these.<br> 
                            
                            <br>Ideally, people with tattoos are advised to wait an year before donating, but three months at minimum is totally okay!<br>
                            
                            <br><h4>4. Weight</h4><br>
                            <div class = "text-center" style='text-align:center;'>
                                <img src="img/b6.avif" alt="weight" height="500"><br>
                                </div>
                                <br><br>The donor should not weigh less than 45-50 Kilograms or 100 pounds.This is so the volume of blood donated will be less than 15 per cent of your circulating blood volume. Taking a greater proportion would cause problems because the body would not be able to adjust quickly enough.<br>
                            
                                <br>The other things that are considered are blood pressure, pulse rate, body temperature, etc. <br>
                            
                            <br><h4>5. pre-existing conditions</h4><br>
                            <div class = "text-center" style='text-align:center;'>
                                <img src="img/b7.avif" alt="Sickness" height="500"><br>
                                </div>
                                <br><br>Diabetic people and those that are taking insulin injections to manage it cannot usually donate. however, if the blood glucose level is under control, it is possible. <br>
                            
                            <br>A person is not eligible if they have hepatitis B, hepatitis C, tuberculosis, leprosy, human immuno-deficiency virus (HIV), heart disease, epilepsy, bleeding disorders, thalassemia, sickle cells anemia and cancer.<br>
                            
                            <br>Common health bugs such as old, flu, sore throat, cold sore, stomach infection or any other infections also hinder the process.<br>
                            
                            <br><br><h3><b>How to prepare?</b></h3><br>
                            <div class = "text-center" style='text-align:center;'>
                            <img src="img/b8.jpeg" alt="Dr. Dog" height="500"><br>
                            </div><br>
                            <br><br>1. Make sure you're well hydrated before! <br>Drink plenty of water or fruit juice the night and morning before you donate. <br>
                            <br>The leading cause of faintness and dizziness is a drop in blood pressure. Getting lot of water or fruit juice into your body helps keep your blood pressure up. It is also crucial if you're donating plasma or platelets. Avoid drinking caffeinated drinks as these will cause your body to expel water. <br><br>
                            
                            2. Eat three hours prior to the donation.<br>
                            It's not a good idea to come with an empty stomach. Eating will keep your blood sugar levels stable, helping you to feel better after you have donated blood.<br> 
                            If you think you might encounter nausea during donation don't eat immediately before donation.<br><br> 
                            
                            3. Wear loose and comfortable clothing and avoid tight sleeves.<br><br>
                            
                            4. Don't take aspirin for two days prior to donating if you are a platelet donor. <br><br>
                            
                            5. Answer questions from the staff members or on forms honestly.<br>
                            Certain activities increase the chances of blood borne illnesses and those who have recently engaged in such activities may be prevented from donating blood for a period of time.<br><br> </p>
